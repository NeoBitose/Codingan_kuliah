def Convert_Time(sec):
    global Timer
    mins = sec // 60    # MINUTES
    sec = sec % 60      # SECONDS
    hours = mins // 60  # HOURS
    mins = mins % 60    # MINUTES
    Timer[2], Timer[3], Timer[4] = hours, mins, sec
    return (f"Time : {int(hours)}.{int(mins)}.{int(sec)}")

